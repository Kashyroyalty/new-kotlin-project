fun main(args: Array<String>) {

    print("Enter floor ")

    var number:Int = readLine()!!.toInt()
    when(number){
        1->  println("Go To First Floor")
        2->  println("Go To Second Floor")
        3->  println("Go To Third Floor")
        4->  println("Go To Fourth Floor")

          else->  println("Floor Not Available")

    }









}